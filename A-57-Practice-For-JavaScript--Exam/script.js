function isLeapYear  (year) {
if( (year % 4 ===0 &&  year % 100  !==0) || year % 400 ===0 ){
    return "Leap Year"
}

else{
    return " Not  A Leap Year "
}
}
const output = 2023
console.log(isLeapYear(output));
console.log(isLeapYear(2024));
console.log(isLeapYear(2025));

console.log(isLeapYear(2000));


