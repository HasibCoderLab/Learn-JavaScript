function startsWithWord(str, word) {
    if (str.startsWith(word)) {
        return `Starts with '${word}'`;
    } else {
        return `Does not start with '${word}'`;
    }
}

console.log(startsWithWord("Hello world", "Hello")); // "Starts with 'Hello'"
console.log(startsWithWord("JavaScript is fun", "Java")); // "Starts with 'Java'"
console.log(startsWithWord("Python is great", "Java")); // "Does not start with 'Java'"



